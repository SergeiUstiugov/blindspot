import numpy as np


# ════════════════════════════════════════════════════════════════════════════
# БЛОК 1. МЕТРИКА + SELFTEST  (рабочий код, копируйте к себе)
# ════════════════════════════════════════════════════════════════════════════

def independence_report(miss, n_boot=500, seed=0):
    """miss: (n, 2) бинарная матрица промахов двух инструментов В ОДНОМ классе
    дефекта (1 = инструмент пропустил настоящий дефект).
    Возвращает (phi, ratio, shuffle_null):
      phi   — φ-корреляция промахов (≈0 при независимости);
      ratio — joint / product: во сколько раз совместный промах чаще, чем при
              независимости (≈1 при независимости, >1 при общем слепом пятне);
      null  — shuffle-контроль: уровень φ при разрушенной связи (оценка пола)."""
    rng = np.random.default_rng(seed)
    a, b = miss[:, 0], miss[:, 1]
    phi = np.corrcoef(a, b)[0, 1] if a.std() and b.std() else np.nan
    joint = float(np.mean(a & b))
    prod = float(a.mean() * b.mean())
    ratio = joint / prod if prod > 0 else np.nan
    null = float(np.mean([
        np.corrcoef(a, rng.permutation(b))[0, 1] if a.std() and b.std() else 0.0
        for _ in range(n_boot)]))
    return phi, ratio, null


def bootstrap_ci(miss, fn, n_boot=1000, seed=1, alpha=0.05):
    """Перцентильный доверительный интервал статистики fn(miss) по ресэмплу строк."""
    rng = np.random.default_rng(seed)
    n = len(miss)
    vals = []
    for _ in range(n_boot):
        v = fn(miss[rng.integers(0, n, n)])
        if v == v:  # не nan
            vals.append(v)
    return (float(np.percentile(vals, 2.5)), float(np.percentile(vals, 97.5)))


def _phi(miss):
    a, b = miss[:, 0], miss[:, 1]
    return np.corrcoef(a, b)[0, 1] if a.std() and b.std() else np.nan


def _ratio(miss):
    a, b = miss[:, 0], miss[:, 1]
    joint = float(np.mean(a & b))
    prod = float(a.mean() * b.mean())
    return joint / prod if prod > 0 else np.nan


def verdict(miss):
    """Вердикт по паре: независимы только если ОБА ДИ накрывают нейтраль
    (φ-ДИ накрывает 0 И ratio-ДИ накрывает 1). Иначе — зависимы/неопределённо."""
    phi, ratio, _ = independence_report(miss)
    phi_ci = bootstrap_ci(miss, _phi)
    ratio_ci = bootstrap_ci(miss, _ratio)
    indep = (phi_ci[0] <= 0 <= phi_ci[1]) and (ratio_ci[0] <= 1 <= ratio_ci[1])
    return indep, phi, phi_ci, ratio, ratio_ci


def _show(label, miss):
    n = len(miss)
    indep, phi, phi_ci, ratio, ratio_ci = verdict(miss)
    tag = "НЕЗАВИСИМЫ" if indep else "ЗАВИСИМЫ"
    if n < 10:
        tag += " / НЕОПРЕДЕЛЁННО (n мал)"
    print(f"\n-- {label}  (n={n}) " + "-" * 36)
    print(f"  phi-corr = {phi:+.3f}  CI [{phi_ci[0]:+.3f}, {phi_ci[1]:+.3f}]")
    print(f"  ratio    = {ratio:.2f}  CI [{ratio_ci[0]:.2f}, {ratio_ci[1]:.2f}]")
    print(f"  marginal: A={miss[:,0].mean():.2f}  B={miss[:,1].mean():.2f}")
    print(f"  ВЕРДИКТ: {tag}")
    return indep


def selftest():
    """Инструмент обязан и пройти, и провалить: независимую пару назвать
    независимой, зависимую — зависимой. Иначе ему нельзя доверять."""
    print("=" * 60)
    print("БЛОК 1. SELFTEST  (рабочий измеритель на синтетике)")
    print("=" * 60)
    rng = np.random.default_rng(0)
    n = 120
    # A — заведомо независимые промахи
    A = np.column_stack([rng.random(n) < 0.3, rng.random(n) < 0.4]).astype(int)
    a_ok = _show("A: заведомо НЕЗАВИСИМЫ (ожидание: независимы)", A)
    # B — общий латентный фактор «трудный дефект»: иногда промахиваются вместе
    hard = rng.random(n) < 0.35
    B = np.column_stack([rng.random(n) < 0.15, rng.random(n) < 0.15]).astype(int)
    B[hard, :] = 1
    b_ok = _show("B: заведомо ЗАВИСИМЫ (ожидание: зависимы)", B)
    ok = a_ok and not b_ok
    print(f"\n  SELFTEST {'ПРОЙДЕН' if ok else 'ПРОВАЛЕН'}: "
          f"{'инструмент различает оба случая' if ok else 'НЕ доверять'}")
    return ok


